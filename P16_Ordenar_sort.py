#Ordena la siguiente lista en orden alfabético descendente (de la letra 'z' a la 'a').

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron',
          'lila', 'negro', 'rosa', 'blanco', 'naranja']  #creamos lista desordenada

colores.sort(reverse=True) #ordenamos con reverse para invertir el olden alfabetico

print(colores) #mostramos lista ya ordenada 
